This module enforces the following NIST 800-53 controls on a GCS bucket:
- SC-12: Cryptographic key establishment via customer-managed KMS key
- SC-13: Cryptographic protection at rest with 90-day key rotation
- SC-28: Protection of information at rest via CMEK AES-256
- AU-11: Audit record retention via configurable retention policy
- CM-6: Configuration settings via required compliance labels